#include "libft.h"

int		ft_atoi(const char *str)
{
	int		n;
	int		sign;

	n = 0;
	if (!str || (str[0] == '\0'))
		return (0);
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	sign = (*str == '-') ? -1 : 1;
	str = (*str == '+' || *str == '-') ? str + 1 : str;
	while (*str >= '0' && *str <= '9')
		n = (n * 10) + (*str++ - '0');
	return (n * sign);
}

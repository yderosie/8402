#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	char		*i;
	const char	*j;

	i = s1;
	j = s2;
	while (n--)
	{
		*i++ = *j++;
	}
	return (s1);
}

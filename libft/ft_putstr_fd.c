#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	int		cmp;

	cmp = 0;
	while (s[cmp] != '\0')
	{
		ft_putchar_fd(s[cmp], fd);
		cmp++;
	}
}

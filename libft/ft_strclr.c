#include "libft.h"

void	ft_strclr(char *s)
{
	int		len;
	int		cmp;

	cmp = 0;
	len = ft_strlen(s);
	while (cmp < len)
	{
		*s = '\0';
		s++;
		cmp++;
	}
}
